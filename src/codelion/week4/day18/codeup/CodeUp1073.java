package codelion.week4.day18.codeup;

import java.util.Scanner;

public class CodeUp1073 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        while (true) {
            int input = sc.nextInt();
            if (input == 0) {
                break;
            }
            System.out.println(input);
        }
    }
}
